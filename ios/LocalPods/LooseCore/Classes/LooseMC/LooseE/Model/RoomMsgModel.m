Byte * TableCenterDataToCache(Byte *data) {
    int flueFib = data[0];
    int suraRate = data[1];
    Byte minimum = data[2];
    int pokeBike = data[3];
    if (!flueFib) return data + pokeBike;
    for (int i = pokeBike; i < pokeBike + suraRate; i++) {
        int value = data[i] + minimum;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[pokeBike + suraRate] = 0;
    return data + pokeBike;
}

NSString *StringFromTableCenterData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TableCenterDataToCache(data)];
