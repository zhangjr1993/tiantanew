Byte * MagDataToCache(Byte *data) {
    int testimonyTune = data[0];
    int consul = data[1];
    int bury = data[2];
    if (!testimonyTune) return data + bury;
    for (int i = 0; i < consul / 2; i++) {
        int begin = bury + i;
        int end = bury + consul - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[bury + consul] = 0;
    return data + bury;
}

NSString *StringFromMagData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)MagDataToCache(data)];
}  
