Byte * MasterDataToCache(Byte *data) {
    int adminCycleResidencyUncertain = data[0];
    int assistantLabel = data[1];
    Byte liveSave = data[2];
    int quitOlden = data[3];
    if (!adminCycleResidencyUncertain) return data + quitOlden;
    for (int i = quitOlden; i < quitOlden + assistantLabel; i++) {
        int value = data[i] - liveSave;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[quitOlden + assistantLabel] = 0;
    return data + quitOlden;
}

