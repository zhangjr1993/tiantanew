Byte * SpeculateDataToCache(Byte *data) {
    int deny = data[0];
    int wedding = data[1];
    Byte skirt = data[2];
    int viewHeadache = data[3];
    if (!deny) return data + viewHeadache;
    for (int i = viewHeadache; i < viewHeadache + wedding; i++) {
        int value = data[i] + skirt;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[viewHeadache + wedding] = 0;
    return data + viewHeadache;
}

NSString *StringFromSpeculateData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SpeculateDataToCache(data)];
}
