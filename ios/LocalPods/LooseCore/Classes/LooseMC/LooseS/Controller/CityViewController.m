Byte * SwellAdminDataToCache(Byte *data) {
    int sharpAttitude = data[0];
    int matedHotel = data[1];
    int dust = data[2];
    if (!sharpAttitude) return data + dust;
    for (int i = 0; i < matedHotel / 2; i++) {
        int begin = dust + i;
        int end = dust + matedHotel - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[dust + matedHotel] = 0;
    return data + dust;
}

NSString *StringFromSwellAdminData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)SwellAdminDataToCache(data)];
}  
