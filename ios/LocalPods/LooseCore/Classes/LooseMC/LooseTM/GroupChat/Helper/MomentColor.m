Byte * MinimalDataToCache(Byte *data) {
    int straddle = data[0];
    int ricochet = data[1];
    int positTee = data[2];
    if (!straddle) return data + positTee;
    for (int i = 0; i < ricochet / 2; i++) {
        int begin = positTee + i;
        int end = positTee + ricochet - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[positTee + ricochet] = 0;
    return data + positTee;
}

NSString *StringFromMinimalData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)MinimalDataToCache(data)];
}  
