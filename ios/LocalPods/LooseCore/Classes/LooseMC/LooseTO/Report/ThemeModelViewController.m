Byte * GhostLassDataToCache(Byte *data) {
    int cessAnkle = data[0];
    int horse = data[1];
    Byte sophisticated = data[2];
    int acidLip = data[3];
    if (!cessAnkle) return data + acidLip;
    for (int i = acidLip; i < acidLip + horse; i++) {
        int value = data[i] + sophisticated;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[acidLip + horse] = 0;
    return data + acidLip;
}

NSString *StringFromGhostLassData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)GhostLassDataToCache(data)];
}
