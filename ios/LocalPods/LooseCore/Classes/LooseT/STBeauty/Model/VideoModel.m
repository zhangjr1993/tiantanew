Byte * CurrencyDataToCache(Byte *data) {
    int head = data[0];
    int transmitter = data[1];
    Byte versoInclude = data[2];
    int mTheory = data[3];
    if (!head) return data + mTheory;
    for (int i = mTheory; i < mTheory + transmitter; i++) {
        int value = data[i] - versoInclude;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[mTheory + transmitter] = 0;
    return data + mTheory;
}

NSString *StringFromCurrencyData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)CurrencyDataToCache(data)];
}
